class Solution {
  public:
    void printTriangle(int n) {
        // code here
        int j = 1;
        for(int i=1;i<=n;i++){
            int count = i;
            while(count--){
                cout<<j<<" ";
                j++;
            }cout<<endl;
        }
    }
};

